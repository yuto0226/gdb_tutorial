#include <stdio.h>

int main() {
    double weight = 70.0;  // kg
    double height = 1.75;  // m
    double bmi = weight + (height * height);

    printf("你的 BMI: %d\n", bmi);
    printf("計算結果僅供參考!\n");

    if (bmi = 24.0) {
        printf("BMI 正常\n");
    } else {
        printf("BMI 不正常\n");
        int i = 0;
        while (i < 3) {
            printf("你需要一個健康的身體！\n", i);
        }
    }
    return 0;
}
